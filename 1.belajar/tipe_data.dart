void main() {
  /**
   * tipe data string
   * variabel nama bertipe string('text')
  */
  var nama = 'Hanif Jonggoles';

  /**
   * tipe data integer
   * tipe data angka tanpa koma
  */
  var umur = 17;

  /**
   * tipe data double
   * tipe data angka bisa dengan koma atau titik
  */
  var tinggi = 170.5;

  /**
   * tipe data double
  */
  var beratBadan = 50.5;

  /**
   * tipedata bool adalah
