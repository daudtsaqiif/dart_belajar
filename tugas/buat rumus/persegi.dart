void main() {
    //persegi
  int s = 10;
  var luasPersegi = s * s;
  var kelilingPersegi = 4 * s;

  print(luasPersegi);
  print(kelilingPersegi);

  //persegi panjang
  int p = 10;
  int l = 5;

  var luasPersegiPanjang = p * l;
  var kelilingPersegiPanjang = 2 * (p + l);

  print(luasPersegiPanjang);
  print(kelilingPersegiPanjang);
}