void main() {
  var a = 10;
  var b = 5;
  var c = 12;

/**
 * tanda > lebih besar
 * hasil 
 */
  print(a > b);

  /**
 * tanda < lebih kecil
 * hasil 
 */
  print(a < b);

  /**
 * tanda >= lebih besar sama dengan
 * hasil 
 */
  print(a >= b);

  /**
 * tanda <= lebih kecil sama dengan
 * hasil 
 */
  print(a <= b);

  /**
 * tanda == sama dengan
 * hasil 
 */
  print(a == b);

  /**
 * tanda != tidak sama dengan
 * hasil 
 */
  print(a != b);

  /**
 * tanda ! negasi(kebalikannya)
 * hasil false
 */
  print(!(a > b));

  /**
 * tanda && dan dan
 * hasil true
 */
  print((a > b) && (a < c));

   /**
 * tanda && dan dan
 * hasil true
 */
  print((a > b) || (a > b));
}
